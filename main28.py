# Входные данные
n = 10  # Количество чисел
a = [12, 25, 30, 45, 50, 55, 60, 75, 80, 95]  # Список чисел
p = 50  # Число для сравнения (задача а)
k = 5   # Число для проверки кратности (задача в)

# а) Количество чисел, которые больше p
count_greater_than_p = sum(1 for num in a if num > p)
print("а) Количество чисел, которые больше p:", count_greater_than_p)

# б) Количество чисел, которые оканчиваются на 5
count_ends_with_5 = sum(1 for num in a if abs(num) % 10 == 5)
print("б) Количество чисел, которые оканчиваются на 5:", count_ends_with_5)

# в) Количество чисел, которые кратны k
count_multiple_of_k = sum(1 for num in a if num % k == 0)
print("в) Количество чисел, которые кратны k:", count_multiple_of_k)
