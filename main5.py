# Ввод диапазона
start = int(input("Введите начало диапазона (трехзначное число): "))
end = int(input("Введите конец диапазона (трехзначное число): "))

# Проверка, что введены трехзначные числа
if start < 100 or end > 999:
    print("Ошибка: введите трехзначные числа.")
else:
    # Поиск чисел, удовлетворяющих условиям
    for number in range(start, end + 1):
        if number % 47 == 43 and number % 43 == 4:  # Исправленные условия
            print(number)
