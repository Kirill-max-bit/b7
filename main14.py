# Пример списка вещественных чисел
numbers = [10.5, 11.0, 10.8, 9.5, 12.3, 10.75, 11.1, 10.6]

# Инициализация переменной для хранения суммы
sum_greater_than_10_75 = 0

# Перебор всех чисел в списке
for num in numbers:
    if num > 10.75:
        sum_greater_than_10_75 += num

# Вывод результата
print("Сумма чисел, больших 10.75:", sum_greater_than_10_75)
