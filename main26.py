# Данные о температуре воздуха за месяц
temperatures = [5, -2, 3, -1, 0, -3, 4, -5, 2, -4, 1, -2, 0, -1, 3, -3, 4, -2, 5, -1, 0, -4, 2, -3, 1, -2, 0, -1, 3, -5]

# Подсчет количества дней с температурой ниже 0°C
count_below_zero = sum(1 for temp in temperatures if temp < 0)
print("Количество дней с температурой ниже 0°C:", count_below_zero)
