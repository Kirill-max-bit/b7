# Пример данных: списки стоимостей автомобилей и мотоциклов
cars = [15000, 20000, 25000, 30000]  # Стоимости автомобилей
motorcycles = [4000, 5000, 3000, 4500]  # Стоимости мотоциклов

# Проверка условия, что стоимость каждого автомобиля больше $5000
if all(car > 5000 for car in cars):
    print("Условие: стоимость каждого автомобиля больше $5000 — выполнено.")
else:
    print("Условие не выполнено. Проверьте данные.")

# Проверка условия, что стоимость любого мотоцикла меньше $5000
if all(motorcycle < 5000 for motorcycle in motorcycles):
    print("Условие: стоимость любого мотоцикла меньше $5000 — выполнено.")
else:
    print("Условие не выполнено. Проверьте данные.")

# Вычисление средней стоимости автомобилей
average_car_cost = sum(cars) / len(cars)

# Вычисление средней стоимости мотоциклов
average_motorcycle_cost = sum(motorcycles) / len(motorcycles)

# Проверка, превышает ли средняя стоимость автомобилей среднюю стоимость мотоциклов более чем в 3 раза
if average_car_cost > 3 * average_motorcycle_cost:
    print(f"Средняя стоимость автомобилей ({average_car_cost}) превышает среднюю стоимость мотоциклов ({average_motorcycle_cost}) более чем в 3 раза.")
else:
    print(f"Средняя стоимость автомобилей ({average_car_cost}) НЕ превышает среднюю стоимость мотоциклов ({average_motorcycle_cost}) более чем в 3 раза.")
