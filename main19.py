# Предположим, что у нас есть список из 15 вещественных чисел
c = [1.5, 2.3, 4.7, 3.2, 5.1, 6.4, 7.8, 8.9, 9.0, 10.2, 11.3, 12.4, 13.5, 14.6, 15.7]

# Инициализируем переменную для хранения суммы
sum_neg_odd = 0

# Проходим по списку с шагом 1
for i in range(len(c)):
    # Проверяем, является ли индекс нечетным (начиная с 0, c1 будет под индексом 0, c3 под индексом 2 и т.д.)
    if i % 2 == 0:  # Индексация в Python начинается с 0, поэтому нечетные позиции соответствуют четным индексам
        sum_neg_odd -= c[i]

# Выводим результат
print("Результат:", sum_neg_odd)
