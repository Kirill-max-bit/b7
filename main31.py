# Пример списка команд с количеством выигрышей и проигрышей
teams = [
    (5, 3),  # Команда 1: 5 выигрышей, 3 проигрыша
    (2, 4),  # Команда 2: 2 выигрыша, 4 проигрыша
    (6, 2),  # Команда 3: 6 выигрышей, 2 проигрыша
    (3, 3),  # Команда 4: 3 выигрыша, 3 проигрыша
    (7, 1),  # Команда 5: 7 выигрышей, 1 проигрыш
]

# Инициализация счетчика
count_teams_with_more_wins = 0

# Проход по списку и подсчет
for wins, losses in teams:
    if wins > losses:
        count_teams_with_more_wins += 1

# Вывод результата
print(f"Количество команд с большим числом выигрышей, чем проигрышей: {count_teams_with_more_wins}")
