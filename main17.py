# Пример списка целых чисел
numbers = [10, 9, 12, 7, 15]

# Ввод числа n
n = int(input("Введите число n: "))

# Инициализация переменной для хранения суммы кратных чисел
sum_multiples = 0

# Перебор всех чисел в списке
for num in numbers:
    if num % n == 0:  # Проверка на кратность n
        sum_multiples += num

# Вывод результата
print(f"Сумма чисел, кратных {n}: {sum_multiples}")
